#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s)
	{
		while (i < ft_strlen(s))
			write(fd, &s[i++], 1);
		write(fd, "\n", 1);
	}
}
